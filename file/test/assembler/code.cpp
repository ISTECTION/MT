int main () {
    int a = 4;                              /// 4
    int b = 2;                              /// 2

    int f = (a + b) << ( (a + 1) & 1 );     /// 6 << (101 & 001) = 12

    char h = 2;                             /// 2

    h <<= b + ( (f - 11) | 2 );             /// 2 <<= (2 + (01 | 10)) = 2^1 * 2^5 = 2^6 = 64

    char i = (h > f) << 3;                  /// (64 > 12) << 3 = 1 << 3 = 2^3 = 8

    int z = (3 > 2);                        /// 3 > 2 = 1

    z |= 6;                                 /// 001 | 110 = 111 = 7
}